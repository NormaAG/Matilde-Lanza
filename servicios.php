								<p style="font-size:14pt;"><span class="image left"><img src="images/pic12.jpg" alt="" /></span>“El objetivo del concurso municipal es incentivar y fomentar a las(os) niñas(os),que participan en las distintas entradas folklóricas y otras manifestaciones culturales que se realizan en el Municipio de Cochabamba”.</p><br>
