									<p style="font-size:14pt;"><span class="image left"><img src="images/pic20.jpg" alt="" /></span>Las piezas, composiciones e interpretaciones, pueden estar en cualquier ritmo nacional. La organización subraya que no se aceptarán temas compuestos por extranjeros adaptados a ritmos del país. La presentación de las bandas en público se realiza cada 5 de octubre desde las 8:00 am, en el Estadium Hipolito. Cada conjunto tendrá 15 minutos y podrá tocar tres canciones. El jurado calificador esta integrado por representantes de la Secretaría de Culturas, además de un músico invitado especialista. Los encargados de calificar a los concursantes tomarán en cuenta el vestuario, coreografía, afinación, seguridad, coordinación rítmica y la autoría de las piezas ejecutadas. </p>
